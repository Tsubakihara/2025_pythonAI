# 問題2: 空クラスを使って柔軟な設定オブジェクトを作ろう
# ---------------------------------------------
# 【背景】
# 業務システムではまだ仕様が決まっていないが、将来的に設定項目が増える場合があります。
# その際「空クラス」を作成し、必要に応じて属性やメソッドを動的に追加することがあります。
#
# 【今回学ぶポイント】
# ・空クラスの書き方（pass文の意味）
# ・動的に属性・メソッドを追加できる柔軟性
#
# 【指示】
# ・空クラスConfigを定義し、pass文で空の本体にしてください。
# ・インスタンスにdb_hostやdb_port属性を動的に追加してください。
# ・簡単な動的メソッドをlambda式で追加し、呼び出してみましょう。
#
# 以下の穴埋めを完成させてください。
# ---------------------------------------------

class Config:
    XXXXXXXXXXXX  # 空クラスを定義するために必要なコード

conf = Config()

conf.db_host = "localhost"
conf.db_port = 3306

conf.get_db_info = lambda: f"{conf.db_host}:{conf.db_port}"

print("Configインスタンスの型:", type(conf))
print("DBホスト:", conf.db_host)
print("DBポート:", conf.db_port)
print("DB接続情報:", conf.get_db_info())
